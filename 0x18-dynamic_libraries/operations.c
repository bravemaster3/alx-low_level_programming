/**
 * add - sums up two integers
 * @a: first integer
 * @b: second integer
 * Return: the sum
 */
int add(int a, int b)
{
    return (a + b);
}

/**
 * sub - subtracts two integers
 * @a: first integer
 * @b: second integer
 * Return: the subtraction
 */
int sub(int a, int b)
{
    return (a - b);
}

/**
 * mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: the product
 */
int mul(int a, int b)
{
    return (a * b);
}

/**
 * div - divides two integers
 * @a: first integer
 * @b: second integer
 * Return: the result of the division
 */
int div(int a, int b)
{
    return (a / b);
}

/**
 * mod - calculates the modulo of two integers
 * @a: first integer
 * @b: second integer
 * Return: the rest of the integer division
 */
int mod(int a, int b)
{
    return (a % b);
}
